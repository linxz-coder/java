package academy.learnprogramming;

public class Main {

    public static void main(String[] args) {
        int myVariable = 50; //myVariable = 50 is an expression, the entire line is a statement.
        myVariable++; //a statement
        myVariable--; //a statement
        System.out.println("This is a test"); //a statement

        System.out.println("This is" +
                "another" +
                "still more."); //a statement does not have to on the same line, a ';' is the end of a statement
    }
}
